          <br><br>
           <h2>Objective of the Project</h2> 
          <h5 class=" text-muted ali"> The main Objective of the project is to develop a Web application on "Rampally Dayara Property Tax Management System". Web-app which maintains the tariff records and keeps the public informed. The Sanctioned Project will be given to "Rampally Dayara Village" as part of UBA Scheme. </h5>
          <br><br>
          <h2>Description of the Project</h2> 
         <h5 class="text-muted ali"> The Rampally Dayara Village Panchayat is facing problem with property tax assessment. The physical records to be maintained are enormous it has been tedious for the management to keep track of all the details of tax payers and their taxes; as the result the transparency between the villagers and the village panchayat is declining at an alarming rate. </h5>
         <h5 class="text-muted ali"> The website would be helpful for maintaining the information of the tax payers who paid the amount and the amount they need to pay later. The proposed solution will avoid frauds and provide security to the information. Also, the application will be up to date according to the government norms. </h5>
